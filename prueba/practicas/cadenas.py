#Cadenas 
#Volver a una cadena a mayuscula metodo upper() y la inversa seria lower()
i = input("Ingrese una cadena de texto ")

print("Escribio", i.upper())
print("Escribio", i.lower())
#El capitalize() Sirve para poner la primera letra en mayusucula
print("Escribio", i.capitalize())
#Hay formas de comprobar si una cadena tiene algun tipo de datos espeficios
print("Escribio", i.isdigit())
#El metodo split sirve para separ en disitintas formas nos devuelve un arreglo lo que tenga adentro lo sepeara 
print("Escribio", i.split(" "))